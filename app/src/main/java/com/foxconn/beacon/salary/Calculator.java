public class Calculator {
    public int sum(int a, int b) {

        return a + b;
    }

    public int divide(int a, int b) {
        return a / b;
    }

    public double multiply(double a, double b) {
        return a * b;
    }

    public int subtract(int a, int b) {
        return a - b;
    }
}
